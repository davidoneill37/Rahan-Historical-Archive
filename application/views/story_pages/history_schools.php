           <br>
           <p>The very first schools in Rahan were possibly the schools at the monasteries of Lynally and Rahan. In more recent times there were Hedge Schools in Killina, Roscore, Killeranny, Clonshanny, Newtown, Cloonagh and Tullabeg. The name ‘Hedge School’ gives the impression of children being taught out in the open air, but in reality, classes were generally held in a building. The conditions of these buildings often left a lot to be desired, as a Board of Education Report of the 1830’s shows. Here we see a listing of the schools in Rahan at the time, who the teacher was, the ethos of the school, the condition of the building, and the numbers of pupils. Over time all of these Hedge schools faded away as a national educational system came into being.</br>
			<br> 
           <p>By 1936, there were 4 national schools in the parish. These schools were all catholic run. Church of Ireland children from the parish, either attended these schools or had to travel further afield to perhaps Liss, Clara or Tullamore.</p>
