           <br><br>
		   <p>The school in Killina, built by Ms. Maria O’Brien was run by the Presentation Sisters. Over time the school was extended to allow for growing numbers. In 1965, a brand new school was built in the convent grounds, to cater for both primary and secondary students. However, as in the past, the numbers of students soon outgrew the building. In the years that followed, there would be much additional building work.</p>
